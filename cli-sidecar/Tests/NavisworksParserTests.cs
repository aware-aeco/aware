    [Fact]
    public void MemberPageParser_Event_Carries_Real_Delegate_Type()
    {
        // Defensive checklist #1 — events must have REAL delegate type, never a
        // synthesized placeholder like "EventHandler" alone.
        var (dll, xml) = FixturePaths();
        if (!File.Exists(dll) || !File.Exists(xml)) return;
        var asm = MetadataReflector.Reflect(dll);
        var docDict = XmlDocLoader.LoadFromFile(xml);
        var app = asm.Types.Single(t => t.Name == "Application");
        var skel = PageParser.BuildSkeleton(app, docDict);
        MemberPageParser.Fill(skel, app, docDict);
        var ev = skel.events.Single(e => e.name == "ActiveDocumentChanged");
        Assert.False(string.IsNullOrEmpty(ev.@delegate));
        Assert.Contains("EventHandler", ev.@delegate);
        // Schema-required handler_thread must be one of main|worker|unknown.
        Assert.Contains(ev.handler_thread, new[] { "main", "worker", "unknown" });
    }
