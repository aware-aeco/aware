    [Fact]
    public void Reflect_Overloaded_Constructors_Yield_Distinct_DocIds()
    {
        // DemoProgress has two constructors: () and (string title). Doc ids must differ
        // (per defensive checklist #2 — "Constructor names carry overload disambiguator").
        var (dll, _) = FixturePaths();
        if (!File.Exists(dll)) return;
        var asm = MetadataReflector.Reflect(dll);
        var pb = asm.Types.Single(t => t.Name == "DemoProgress");
        Assert.Equal(2, pb.Constructors.Count);
        var ids = pb.Constructors.Select(c => c.DocId).Distinct().Count();
        Assert.Equal(2, ids);
        Assert.Contains(pb.Constructors,
            c => c.DocId == "M:FixtureAssembly.NavisworksLike.DemoProgress.#ctor");
        Assert.Contains(pb.Constructors,
            c => c.DocId == "M:FixtureAssembly.NavisworksLike.DemoProgress.#ctor(System.String)");
    }

    [Fact]
    public void Reflect_Overloaded_Methods_Yield_Distinct_DocIds_And_Signatures()
    {
        var (dll, _) = FixturePaths();
        if (!File.Exists(dll)) return;
        var asm = MetadataReflector.Reflect(dll);
        var app = asm.Types.Single(t => t.Name == "Application");
        var begins = app.Methods.Where(m => m.Name == "BeginProgress").ToList();
        Assert.Equal(3, begins.Count);
        // All three doc ids must be distinct (zero-arg, one-arg, two-arg).
        Assert.Equal(3, begins.Select(m => m.DocId).Distinct().Count());
        Assert.Equal(3, begins.Select(m => m.Signature).Distinct().Count());
    }

