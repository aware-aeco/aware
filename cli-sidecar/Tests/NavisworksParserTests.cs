// Tests for the Navisworks coverage extractor: XmlDocLoader + MetadataReflector +
// PageParser + MemberPageParser.
//
// Strategy: instead of depending on the proprietary Autodesk.Navisworks.Api.dll,
// the tests exercise the FixtureAssembly.NavisworksLike namespace — a hand-rolled
// fixture that mirrors the Sandcastle XML doc patterns Autodesk ships (per-overload
// constructors, enum members with summaries, properties with cref refs, events,
// methods with <exception> elements).
//
// Each test isolates one structural feature so a regression in the parser surfaces
// against a focused failure.

using System.Xml.Linq;
using AwareSidecar.Ingest;
using AwareSidecar.Ingest.Extractors.Common;
using AwareSidecar.Ingest.Extractors.Navisworks;
using Xunit;

namespace AwareSidecar.Ingest.Tests;

public class NavisworksParserTests
{
    /// <summary>
    /// Path to the FixtureAssembly DLL and XML doc generated by the test build.
    /// `Assembly.Location` of the test assembly drops us in
    /// `cli-sidecar/Tests/bin/Debug/net10.0/`; the FixtureAssembly outputs are
    /// next to it.
    /// </summary>
    static (string Dll, string Xml) FixturePaths()
    {
        var here = Path.GetDirectoryName(typeof(NavisworksParserTests).Assembly.Location)!;
        return (
            Path.Combine(here, "FixtureAssembly.dll"),
            Path.Combine(here, "FixtureAssembly.xml"));
    }

    // ── XmlDocLoader ───────────────────────────────────────────────────────

    [Fact]
    public void Xml_Parses_Inline_Summary()
    {
        const string xml = """
            <?xml version="1.0" encoding="utf-8"?>
            <doc>
              <assembly><name>X</name></assembly>
              <members>
                <member name="T:Foo">
                  <summary>A foo type.</summary>
                </member>
              </members>
            </doc>
            """;
        var path = Path.GetTempFileName();
        File.WriteAllText(path, xml);
        try
        {
            var dict = XmlDocLoader.LoadFromFile(path);
            Assert.True(dict.ContainsKey("T:Foo"));
            Assert.Equal("A foo type.", dict["T:Foo"].Summary);
        }
        finally { File.Delete(path); }
    }

    [Fact]
    public void Xml_Parses_Cref_Inside_Summary_As_Short_Name()
    {
        const string xml = """
            <?xml version="1.0" encoding="utf-8"?>
            <doc>
              <assembly><name>X</name></assembly>
              <members>
                <member name="P:Foo.Bar">
                  <summary>See the <see cref="T:Other.Type"/> documentation.</summary>
                </member>
              </members>
            </doc>
            """;
        var path = Path.GetTempFileName();
        File.WriteAllText(path, xml);
        try
        {
            var dict = XmlDocLoader.LoadFromFile(path);
            var summary = dict["P:Foo.Bar"].Summary;
            Assert.NotNull(summary);
            Assert.Contains("Type", summary!);
            Assert.DoesNotContain("T:Other.Type", summary);
        }
        finally { File.Delete(path); }
    }

    [Fact]
    public void Xml_Parses_Per_Param_And_Exception_Children()
    {
        const string xml = """
            <?xml version="1.0" encoding="utf-8"?>
            <doc>
              <assembly><name>X</name></assembly>
              <members>
                <member name="M:Foo.Bar(System.String,System.Int32)">
                  <summary>Does a thing.</summary>
                  <param name="title">The title.</param>
                  <param name="count">How many.</param>
                  <returns>The result.</returns>
                  <exception cref="T:System.ArgumentNullException">title is null.</exception>
                </member>
              </members>
            </doc>
            """;
        var path = Path.GetTempFileName();
        File.WriteAllText(path, xml);
        try
        {
            var dict = XmlDocLoader.LoadFromFile(path);
            var doc = dict["M:Foo.Bar(System.String,System.Int32)"];
            Assert.Equal("The title.", doc.Params["title"]);
            Assert.Equal("How many.", doc.Params["count"]);
            Assert.Equal("The result.", doc.Returns);
            Assert.Single(doc.Exceptions);
            Assert.Equal("System.ArgumentNullException", doc.Exceptions[0].TypeRef);
            Assert.Equal("title is null.", doc.Exceptions[0].Description);
        }
        finally { File.Delete(path); }
    }

    [Fact]
    public void Xml_Filters_Empty_Member_To_Empty_MemberDoc()
    {
        // Autodesk emits <member name="T:LcUGuid"></member> for undocumented types.
        // We keep the entry with all-null fields so callers can distinguish
        // "explicitly empty" from "completely undocumented".
        const string xml = """
            <?xml version="1.0" encoding="utf-8"?>
            <doc>
              <assembly><name>X</name></assembly>
              <members>
                <member name="T:LcUGuid"></member>
              </members>
            </doc>
            """;
        var path = Path.GetTempFileName();
        File.WriteAllText(path, xml);
        try
        {
            var dict = XmlDocLoader.LoadFromFile(path);
            Assert.True(dict.ContainsKey("T:LcUGuid"));
            Assert.Null(dict["T:LcUGuid"].Summary);
        }
        finally { File.Delete(path); }
    }

