    [Fact]
    public void PrettyType_Roundtrips_Generic_Instantiation_With_Angle_Brackets()
    {
        // The brief's defensive checklist #3 — generic angle brackets must
        // round-trip back into the catalog name as `<...>`, never `.X.`.
        // Container types preserve their namespace (matching Tekla's mixed style);
        // primitives are shortened to C# keywords.
        Assert.Equal(
            "System.Collections.ObjectModel.Collection<int>",
            MetadataReflector.PrettyType("System.Collections.ObjectModel.Collection{System.Int32}"));
        // Backtick-encoded generic type parameter (`0 = first type param) — PrettyType
        // strips the arity-and-positional encoding because the caller's generic
        // context isn't available at the pretty-printing layer. The resulting empty
        // string inside the brackets is acceptable for the IR's signature display
        // because callers that need the real type-parameter name use the typed
        // parameter records, not the pretty-printed string.
        Assert.Equal(
            "System.Collections.Generic.IEnumerable<>",
            MetadataReflector.PrettyType("System.Collections.Generic.IEnumerable{`0}"));
        // Critically: angle brackets are present, never `.X.` placeholders.
        Assert.DoesNotContain(".X.",
            MetadataReflector.PrettyType("System.Collections.ObjectModel.Collection{System.Int32}"));
    }

    [Fact]
    public void PrettyType_Handles_Arrays()
    {
        Assert.Equal("int[]", MetadataReflector.PrettyType("System.Int32[]"));
        Assert.Equal("string[]", MetadataReflector.PrettyType("System.String[]"));
        Assert.Equal("byte[,]", MetadataReflector.PrettyType("System.Byte[,]"));
    }

    [Fact]
    public void PrettyType_Handles_Nullable()
    {
        Assert.Equal("int?", MetadataReflector.PrettyType("System.Nullable{System.Int32}"));
    }

    // ── PageParser + MemberPageParser end-to-end ───────────────────────────

    [Fact]
    public void PageParser_Skeleton_Fills_Type_Level_Fields_From_XmlDoc()
    {
        var (dll, xml) = FixturePaths();
        if (!File.Exists(dll) || !File.Exists(xml)) return;
        var asm = MetadataReflector.Reflect(dll);
        var docDict = XmlDocLoader.LoadFromFile(xml);
        var app = asm.Types.Single(t => t.Name == "Application");
        var skel = PageParser.BuildSkeleton(app, docDict);
        Assert.Equal("Application", skel.name);
        Assert.Equal("FixtureAssembly.NavisworksLike", skel.@namespace);
        Assert.Equal("class", skel.kind);
        Assert.Equal("The singleton object that represents the application.", skel.summary);
        Assert.Empty(skel.methods);  // Filled by MemberPageParser.
    }

    [Fact]
    public void MemberPageParser_Fills_Method_Params_Returns_Throws()
    {
        var (dll, xml) = FixturePaths();
        if (!File.Exists(dll) || !File.Exists(xml)) return;
        var asm = MetadataReflector.Reflect(dll);
        var docDict = XmlDocLoader.LoadFromFile(xml);
        var app = asm.Types.Single(t => t.Name == "Application");
        var skel = PageParser.BuildSkeleton(app, docDict);
        MemberPageParser.Fill(skel, app, docDict);

        // Two-arg BeginProgress: real params with docs, returns DemoProgress, throws ArgumentNullException.
        var beginTwoArg = skel.methods.Single(m =>
            m.signature.Contains("string title") && m.signature.Contains("string message"));
        Assert.Equal(2, beginTwoArg.@params.Count);
        Assert.Equal("title", beginTwoArg.@params[0].name);
        Assert.Equal("Title for the overall progress operation.", beginTwoArg.@params[0].doc);
        Assert.NotNull(beginTwoArg.returns);
        Assert.Contains("DemoProgress", beginTwoArg.returns!.type);
        Assert.Single(beginTwoArg.throws);
        Assert.Contains("ArgumentNullException", beginTwoArg.throws[0].type);
        Assert.Equal("title is null.", beginTwoArg.throws[0].when);
    }

